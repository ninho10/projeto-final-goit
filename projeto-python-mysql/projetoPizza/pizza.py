class Pizza:
    def __init__(self, id_pizza, preco, sabor):
        self.__id_pizza: id_pizza
        self.__preco: preco
        self.__sabor: sabor


@property
def id_pizza(self):
    return self.__id_pizza


@id_pizza.setter
def id_pizza(self, id_pizza):
    self.__id_pizza = id_pizza


@property
def preco(self):
    return self.__preco


@preco.setter
def preco(self, preco):
    self.__preco = preco


@property
def sabor(self):
    return self.__sabor


@sabor.setter
def sabor(self, sabor):
    self.__sabor = sabor
